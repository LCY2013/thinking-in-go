package main

import (
	"fmt"
	"math/rand"
	"time"
)

type ByteSize float64

const (
	_           = iota // 通过赋值给空白标识符来忽略值
	KB ByteSize = 1 << (10 * iota)
	MB
	GB
	TB
	PB
	EB
	ZB
	YB
)

func main() {
	var n int16 = 34
	// var _ int16 = n++ error
	// ++n error
	var m int32
	// compiler error: cannot use n (type int16) as type int32 in assignment
	//m = n
	m = int32(n)

	fmt.Printf("32 bit int is: %d\n", m)
	fmt.Printf("16 bit int is: %d\n", n)

	v1 := complex(1, 3)
	var v2 complex64 = 2 + 4i
	fmt.Printf("%v %v\n", real(v1), imag(v1))
	fmt.Printf("%v %v\n", real(v2), imag(v2))
	fmt.Printf("%f %f\n", real(v2), imag(v2))
	fmt.Printf("%v \n", v1)
	//big.Int
	//big.Rat{}

	randomInfo()
}

func randomInfo() {
	for i := 0; i < 10; i++ {
		a := rand.Int()
		fmt.Printf("%d / ", a)
	}
	for i := 0; i < 5; i++ {
		r := rand.Intn(8)
		fmt.Printf("%d / ", r)
	}
	fmt.Println()
	timens := int64(time.Now().Nanosecond())
	rand.Seed(timens)
	for i := 0; i < 10; i++ {
		fmt.Printf("%2.2f / ", 100*rand.Float32())
	}
}
